package BasicJavaPrograms;
/*
Here we will find out the number is
prime or not
 */
public class PrimeNumber {
    public static void main(String[] args) {
        int i=3;
        if(i%2==0){
            System.out.println("The number is prime");
        }else{
            System.out.println("The number is not prime");
        }
    }
}
